    private void setValue() {
        emp.setEmpNO(Integer.parseInt(TxtEmpNO.getText()));
        emp.setEmpName(TxtEmpName.getText());
        emp.setSalary(Double.parseDouble(TxtSalary.getText()));
        emp.setAddres(TxtAddres.getText());

        SimpleDateFormat d = new SimpleDateFormat("yyyy-MM-dd");
        String HDate = d.format(TxtHiringDate.getDate());
        String BDate = d.format(TxtBirthDate.getDate());
        emp.setBirthDate(BDate);
        emp.setHiringDate(HDate);

        String DName = cbxDept.getSelectedItem().toString();
        String Deptn = dept.getValueByName(DName);
        emp.setDeptNO(Integer.parseInt(Deptn));
    }
    Employee_Phones phones = new Employee_Phones();

    private void BtnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnAddActionPerformed
        setValue();
        emp.add();

        //AddPhones
        phones.setEmpNO(Integer.parseInt(TxtEmpNO.getText()));
        for (int x = 0; x < TblPhone.getRowCount(); x++) {
            phones.setPhone(TblPhone.getValueAt(x, 0).toString());
            phones.add();
        }

        cleardata();
        emp.getAllRows(TblEmp);

    }//GEN-LAST:event_BtnAddActionPerformed

    private void BtnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnUpdateActionPerformed
        if (Tolls.confirmMsg("Do You Want To Update ?")) {
            setValue();
            emp.update();

            phones.setEmpNO(Integer.parseInt(TxtEmpNO.getText()));
            //delete phone
            phones.deleteByEmp();
            //add
            for (int x = 0; x < TblPhone.getRowCount(); x++) {
                phones.setPhone(TblPhone.getValueAt(x, 0).toString());
                phones.add();
            }

            cleardata();
            emp.getAllRows(TblEmp);
        }
    }//GEN-LAST:event_BtnUpdateActionPerformed

    private void BtnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnDeleteActionPerformed
        if (Tolls.confirmMsg("Do You Want To Delete ?")) {

            phones.setEmpNO(Integer.parseInt(TxtEmpNO.getText()));
            //delete phone
            phones.deleteByEmp();
            setValue();
            emp.delete();
            cleardata();
            emp.getAllRows(TblEmp);
        }
    }//GEN-LAST:event_BtnDeleteActionPerformed

