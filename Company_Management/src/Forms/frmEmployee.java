    private void TblEmpMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TblEmpMouseClicked
        try {
            int row = TblEmp.getSelectedRow();
            TxtEmpNO.setText(TblEmp.getValueAt(row, 0).toString());
            TxtEmpName.setText(TblEmp.getValueAt(row, 1).toString());
            TxtAddres.setText(TblEmp.getValueAt(row, 2).toString());
            TxtSalary.setText(TblEmp.getValueAt(row, 3).toString());

            SimpleDateFormat d = new SimpleDateFormat("yyyy-MM-dd");
            Date HDate = d.parse(TblEmp.getValueAt(row, 4).toString());
            Date BDate = d.parse(TblEmp.getValueAt(row, 5).toString());
            TxtHiringDate.setDate(HDate);
            TxtBirthDate.setDate(BDate);

            cbxDept.setSelectedItem(TblEmp.getValueAt(row, 7));

            phones.setEmpNO(Integer.parseInt(TxtEmpNO.getText()));
            phones.getAllRows(TblPhone);

            BtnAdd.setEnabled(false);
            BtnUpdate.setEnabled(true);
            BtnDelete.setEnabled(true);

        } catch (Exception ex) {
            Tolls.MsgBox(ex.getMessage());
        }
    }//GEN-LAST:event_TblEmpMouseClicked

    private void BtnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnSearchActionPerformed
        String strSelect = "select * from employee_data where ";
        if (RdoNumber.isSelected()) {
            strSelect += "NUMBER = " + TxtSearch.getText();
            emp.getCustomRows(strSelect, TblEmp);
            cleardata();
        } else if (RdoName.isSelected()) {
            strSelect += "NAME like '%" + TxtSearch.getText() + "%'";
            emp.getCustomRows(strSelect, TblEmp);
            cleardata();
        } else if (RdoAddress.isSelected()) {
            strSelect += "ADDRESS like '%" + TxtSearch.getText() + "%'";
            emp.getCustomRows(strSelect, TblEmp);
            cleardata();
        } else if (RdoSalary.isSelected()) {
            strSelect += "SALARY = " + TxtSearch.getText();
            emp.getCustomRows(strSelect, TblEmp);
            cleardata();
        } else if (RdoHiring.isSelected()) {
            strSelect += "HIRING_DATE like '%" + TxtSearch.getText() + "%'";
            emp.getCustomRows(strSelect, TblEmp);
            cleardata();
        } else if (RdoBirth.isSelected()) {
            strSelect += "BIRTH_OF_DATE like '%" + TxtSearch.getText() + "%'";
            emp.getCustomRows(strSelect, TblEmp);
            cleardata();
        } else if (RdoDepartment.isSelected()) {
            strSelect += "DEPATEMENT like '%" + TxtSearch.getText() + "%' or DEPATEMENT_NO like '%" + TxtSearch.getText() + "%'";
            emp.getCustomRows(strSelect, TblEmp);
            cleardata();
        } else if (RdoPhone.isSelected()){
            String strEmpNum = phones.getEmpNOByPhone(TxtSearch.getText());
            strSelect += "Number like '%" + strEmpNum + "%'";
            emp.getCustomRows(strSelect, TblEmp);
           
            //cleardata();
        }


    }//GEN-LAST:event_BtnSearchActionPerformed

